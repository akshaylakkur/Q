  /**
   * Handle a pause request by checkpointing and waiting.
   */
  private async handlePause(orchestrator: OrchestratorCore): Promise<void> {
    // Checkpoint before pausing
    await this.checkpoint(this.campaignState, orchestrator);

    orchestrator.recordAgentEvent({
      type: "high-campaign.pausing",
      campaignId: this.campaignState.campaignId,
      phase: this.campaignState.currentPhase,
      convergenceCount: this.campaignState.convergenceCount,
    });

    // Wait for resume via the orchestrator's pause mechanism
    await orchestrator.pause();

    this.campaignState.pauseRequested = false;

    orchestrator.recordAgentEvent({
      type: "high-campaign.resumed",
      campaignId: this.campaignState.campaignId,
      phase: this.campaignState.currentPhase,
      convergenceCount: this.campaignState.convergenceCount,
    });
  }


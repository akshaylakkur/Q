  /**
   * Emit a progress checkpoint event and record it in the campaign state.
   */
  private emitProgressCheckpoint(
    filesChanged: number,
    orchestrator: OrchestratorCore,
  ): void {
    const checkpoint = {
      convergenceNumber: this.campaignState.convergenceCount,
      timestamp: new Date().toISOString(),
      filesChanged,
      tokensUsed: this.cycle.totalTokens,
    };

    this.campaignState.progressCheckpoints.push(checkpoint);

    // Trim to max size
    if (
      this.campaignState.progressCheckpoints.length > MAX_PROGRESS_CHECKPOINTS
    ) {
      this.campaignState.progressCheckpoints =
        this.campaignState.progressCheckpoints.slice(
          this.campaignState.progressCheckpoints.length -
            MAX_PROGRESS_CHECKPOINTS,
        );
    }

    orchestrator.recordAgentEvent({
      type: "high-campaign.progress-checkpoint",
      campaignId: this.campaignState.campaignId,
      convergenceNumber: checkpoint.convergenceNumber,
      timestamp: checkpoint.timestamp,
      filesChanged: checkpoint.filesChanged,
      tokensUsed: checkpoint.tokensUsed,
      totalPhases: this.phasePlan?.phases.length ?? 0,
      completedPhases: this.campaignState.completedPhases.length,
    });
  }

